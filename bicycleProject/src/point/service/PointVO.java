package point.service;

public class PointVO {

	private int id;
	private String geom;
	private String ctv_nam;
	private String x_geom;
	private String y_geom;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGeom() {
		return geom;
	}

	public void setGeom(String geom) {
		this.geom = geom;
	}

	public String getCtv_nam() {
		return ctv_nam;
	}

	public void setCtv_nam(String ctv_nam) {
		this.ctv_nam = ctv_nam;
	}

	public String getX_geom() {
		return x_geom;
	}

	public void setX_geom(String x_geom) {
		this.x_geom = x_geom;
	}

	public String getY_geom() {
		return y_geom;
	}

	public void setY_geom(String y_geom) {
		this.y_geom = y_geom;
	}

}
