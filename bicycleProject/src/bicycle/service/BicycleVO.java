package bicycle.service;

public class BicycleVO {
	
	private String bicycleNum;
	private String bicycleAddr;
	private String bicycleTel;

	public String getBicycleNum() {
		return bicycleNum;
	}

	public void setBicycleNum(String bicycleNum) {
		this.bicycleNum = bicycleNum;
	}

	public String getBicycleAddr() {
		return bicycleAddr;
	}

	public void setBicycleAddr(String bicycleAddr) {
		this.bicycleAddr = bicycleAddr;
	}

	public String getBicycleTel() {
		return bicycleTel;
	}

	public void setBicycleTel(String bicycleTel) {
		this.bicycleTel = bicycleTel;
	}

}
