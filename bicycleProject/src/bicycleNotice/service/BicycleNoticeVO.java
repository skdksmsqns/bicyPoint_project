package bicycleNotice.service;

public class BicycleNoticeVO {

	private Integer nt_num;
	private String nt_sj;
	private String nt_cn;
	private String nt_usr;
	private String admin_id;

	public Integer getNt_num() {
		return nt_num;
	}

	public void setNt_num(Integer nt_num) {
		this.nt_num = nt_num;
	}

	public String getNt_sj() {
		return nt_sj;
	}

	public void setNt_sj(String nt_sj) {
		this.nt_sj = nt_sj;
	}

	public String getNt_cn() {
		return nt_cn;
	}

	public void setNt_cn(String nt_cn) {
		this.nt_cn = nt_cn;
	}

	public String getNt_usr() {
		return nt_usr;
	}

	public void setNt_usr(String nt_usr) {
		this.nt_usr = nt_usr;
	}

	public String getAdmin_id() {
		return admin_id;
	}

	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}

}
